// Belajar Javascript

// Variabel

// Variabel merupakan kumpulan data yang isinya bisa berupa
// data tulisan atau angka
// atau apapun yang kalian mau asalkan valid

// Contoh

var hewan = "Ayam";
console.log(hewan)

var makanan = "Ikan"
console.log(makanan)

var nasiPadang ="Tempe"
console.log(nasiPadang)

var angkaDua = 2
console.log(angkaDua)

var angkaTiga = 3
console.log(angkaTiga)

// Mengubah isi variabel

var nama = "Fulan"
nama = "Dimas"
console.log(nama)

